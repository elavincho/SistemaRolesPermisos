    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        String usuario = txtUsuario.getText();
        String contrasenia = txtContrasena.getText();
        //String mensaje = control.validarUsuario(usuario, contrasenia);
        Usuario user = control.validarUsuario(usuario, contrasenia);
        
        if (user != null){
            String rol = user.getUnRol().getNombreRol();
            if (rol.equals("admin")){
                /*Instanciamos a la clase PrincipalAdmin y le pasamos como parametro
                a la controladora para no crear varias instancias a la controladora*/
                PrincipalAdmin pAdmin = new PrincipalAdmin(control, user);
                pAdmin.setVisible(true);
                pAdmin.setLocationRelativeTo(null);
                this.dispose();
            }
            if (rol.equals("user")){
                /*Instanciamos a la clase PrincipalUser y le pasamos como parametro
                a la controladora para no crear varias instancias a la controladora*/
                PrincipalUser pUser = new PrincipalUser(control, user);
                pUser.setVisible(true);
                pUser.setLocationRelativeTo(null);
                this.dispose();
            }
        }else{
            txtMensaje.setText("Usuario o Contraseña Incorrecto!");
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

    

